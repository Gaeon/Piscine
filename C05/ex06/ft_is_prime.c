int	ft_is_prime(int nb)
{
	int	flag;
	int	num;

	flag = 0;
	num = 2;
	if (nb < 2)
		return (0);
	while (num * num <= nb && num < 46341)
	{
		if(nb % num == 0)
			return (0);
		num++;
	}
	return (1);
}